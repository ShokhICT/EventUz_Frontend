export const useI18n = () => {
  if (process.client) {
    const saved = localStorage.getItem('eventhub_locale')
    if (saved && ['uz', 'ru', 'en'].includes(saved)) {
      currentLocale.value = saved
    }
  }

  const t = (key: string) => {
    const keys = key.split('.')
    let current = translations[currentLocale.value]
    for (const k of keys) {
      if (!current || current[k] === undefined) return key
      current = current[k]
    }
    return current
  }

  const setLocale = (locale: string) => {
    if (['uz', 'ru', 'en'].includes(locale)) {
      currentLocale.value = locale
      if (process.client) {
        localStorage.setItem('eventhub_locale', locale)
      }
    }
  }

  const getLocale = () => currentLocale.value

  return { t, setLocale, currentLocale }
}
